import Foundation

public class Calculator {
    public init() {}
    
    public func sum(x: Float, y: Float) -> Float {
        return x + y
    }
    public func subtract(x: Float, y: Float) -> Float {
        return x - y
    }
    public func mutiply(x: Float, y: Float) -> Float {
        return x * y
    }
    public func divide(x: Float, y: Float) -> Float {
        return x / y
    }
}
